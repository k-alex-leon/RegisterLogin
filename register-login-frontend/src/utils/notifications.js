import { toast, Bounce } from "react-toastify";

export const notifySuccess = (msg) => {
  return toast.success(msg, {
    position: "bottom-center",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    delay : 500,
    theme: "light",
    transition: Bounce,
  });
};

